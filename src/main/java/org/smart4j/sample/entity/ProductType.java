package org.smart4j.sample.entity;

import org.smart4j.framework.orm.BaseEntity;

public class ProductType extends BaseEntity {

    private long id;

    private String name;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
